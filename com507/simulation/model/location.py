class Location:
    def __init__(self, x:int, y:int):
        self.x = x
        self.y = y

    def get_x(self) -> int:
        return self.x
    
    def set_x(self, x:int):
        self.x = x

    def get_y(self) -> int:
        return self.y

    def set_y(self, y:int):
        self.y = y

        